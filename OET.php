                    <p>Components Of OET</p>
                    <h3>Listening</h3>
                    <p>Listening subtest</p>
                    <ul>
                        <li>42 questions make up the new listening subtest, which is divided into Parts A, B, and C. The listening session lasts about 40 minutes in total. Your language and listening comprehension, as well as your understanding of other accents, will be examined. To determine the answers, a candidate can predict, identify, paraphrase, use synonyms, and provide precise information. For all 12 occupations, the listening sub-test is the same and involves some multitasking. You must listen to the talk and write at the same time.</li>
                    </ul>
                    <p>Listening Part A</p>
                    <ul>
                        <li>This section lasts approximately 15 minutes. Part A has 24 questions in total, 12 for each of the two extracts (doctor and patient consultations). Before each extract, you will have 30 seconds to read the content. The question type is a sentence completion question.</li>
                    </ul>
                    <p>Listening Part B</p>
                    <ul>
                        <li>Six recordings for six multiple-choice questions are included in Part B. Each audio lasts between 40 and 60 seconds. Each workplace extract contains one multiple-choice question with three alternative responses.</li>
                    </ul>
                    <p>Listening Part c</p>
                    <ul>
                        <li>You will hear two recordings of healthcare professionals (presenting or talking about health) throughout the final 15 minutes of the hearing subtest. Similar to Part B, there are 12 questions, and there are three alternative solutions.</li>
                    </ul>
                    <h3>Reading</h3>
                    <p>Reading Subtest</p>
                    <ul>
                        <li>There are a total of 42 questions in the three portions (A, B, and C) of the Reading subtest. The subtest lasts for a total of 60 minutes. Additionally, it is broken down into 45 minutes for parts B and C and 15 minutes for portion A.</li>
                    </ul>
                    <p>Reading Part A</p>
                    <ul>
                        <li>This section consists of 20 questions (worth 20 marks), with a variety of question types, including sentence completion, paragraph identification, and question-answer types. A candidate can utilize scanning and skimming to find the information they need.</li>
                    </ul>
                    <p>Reading Part B</p>
                    <ul>
                        <li>There are 6 questions in this section, each worth one point. Each has a three-option multiple-choice question. You must choose the correct answer from each of the paragraphs that follow the question.</li>
                    </ul>
                    <p>Reading Part C</p>
                    <ul>
                        <li>This section consists of two lengthy passages and 16 questions (for a total of 16 marks). Each of the questions in this section will have four possible answers.</li>
                    </ul>
                    <h3>Writing</h3>
                    <p>Writing Sub test</p>
                    <ul>
                        <li>The profession-specific Writing subtest takes 45 minutes. Based on a normal office environment and the requirements of the profession, one task is established for each one. A nurse completes the task for nursing, a dentist completes the assignment for dentistry, and so on.</li>
                    </ul>
                    <h3>Speaking</h3>
                    <p>Speaking Sub test</p>
                    <ul>
                        <li>The speaking portion of the test lasts for roughly 20 minutes. Two role plays based on typical working scenarios must be completed as part of a profession-specific test. The candidates use resources created specifically for their line of work.</li>
                    </ul>
